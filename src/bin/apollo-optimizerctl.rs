static VERSION_CHECKED: AtomicBool = AtomicBool::new(false);

fn socket_candidates() -> [&'static str; 2] {
    [
        "/var/run/apollo-optimizer.sock",
        "/tmp/apollo-optimizer.sock",
    ]
}

#[derive(Parser)]
#[command(name = "apollo-optimizerctl")]
struct Cli {
    #[command(subcommand)]
    command: Commands,
}

#[derive(Subcommand)]
enum Commands {
    /// Visual dashboard with system status, gauges, and verdict
    Dashboard,
    Status,
    Metrics,
    TopBlockers,
    ProfileTimeline,
    Doctor,
    Capabilities,
    Restore,
    PanicRestore,
    /// Pause all optimization (creates kill switch file)
    Pause,
    /// Resume optimization (removes kill switch file)
    Resume,
    /// Check if Apollo is paused
    IsPaused,
    SetAutoProfile {
        #[arg(value_parser = ["on", "off"])]
        enabled: String,
    },
    ClearProfileOverride,
    SetProfile {
        #[arg(value_parser = ["balanced-root", "aggressive-root", "safe-root"])]
        profile: String,
        #[arg(long, default_value_t = 20)]
        ttl_minutes: u64,
    },
    SetLatencyTarget {
        #[arg(value_parser = ["low", "normal", "max"])]
        target: String,
    },
    Llm {
        #[command(subcommand)]
        command: LlmCommands,
    },
    DumpPolicy,
    Feedback {
        #[arg(value_parser = ["good", "bad"])]
        rating: String,
        #[arg(long)]
        note: Option<String>,
    },
    Usage {
        #[command(subcommand)]
        command: UsageCommands,
    },
    /// Use Claude Code as LLM Teacher (no API key needed)
    Teach {
        #[command(subcommand)]
        command: TeachCommands,
    },
    /// Show reactive sysctl governor status
    SysctlGovernor,
    /// Revert all sysctl changes made by the daemon to their startup defaults
    RevertSysctls,
    /// Show daemon protocol version and build info
    Version,
    /// Show circuit breaker and degradation health summary
    Health,
}

#[derive(Subcommand)]
enum UsageCommands {
    Top {
        #[arg(long, default_value_t = 10)]
        limit: usize,
    },
    Explain {
        name: String,
    },
}

#[derive(Subcommand)]
enum TeachCommands {
    /// Export usage model + policy for Claude to analyze
    Export,
    /// Apply a learned_policy.json generated by Claude
    Apply {
        /// Path to the learned_policy.json file
        file: String,
    },
}

#[derive(Subcommand)]
enum LlmCommands {
    Status,
    Disable,
    Test,
    SetKey {
        /// API key value. If omitted, reads from APOLLO_LLM_API_KEY.
        #[arg(long)]
        key: Option<String>,
        /// Training TTL (days). After TTL, daemon deletes key and stops using LLM.
        #[arg(long, default_value_t = 30)]
        ttl_days: u64,
    },
}

fn to_profile(s: &str) -> OptimizationProfile {
    match s {
        "aggressive-root" => OptimizationProfile::AggressiveRoot,
        "safe-root" => OptimizationProfile::SafeRoot,
        _ => OptimizationProfile::BalancedRoot,
    }
}

fn to_latency_target(s: &str) -> LatencyTarget {
    match s {
        "max" => LatencyTarget::Max,
        "low" => LatencyTarget::Low,
        _ => LatencyTarget::Normal,
    }
}

/// Low-level send: connects, sends request, reads one response line.
/// Does NOT trigger version checking — used by `check_version_once()` to avoid recursion.
fn send_raw(req: DaemonRequest) -> anyhow::Result<DaemonResponse> {
    let mut stream = None;
    for path in socket_candidates() {
        if let Ok(s) = UnixStream::connect(path) {
            stream = Some(s);
            break;
        }
    }
    let mut stream = stream.context("cannot connect to daemon socket")?;
    stream.set_read_timeout(Some(std::time::Duration::from_secs(30)))?;
    stream.set_write_timeout(Some(std::time::Duration::from_secs(5)))?;
    let payload = serde_json::to_string(&req)?;
    writeln!(stream, "{}", payload)?;

    let mut reader = BufReader::new(stream);
    let mut line = String::new();
    const MAX_RESPONSE_BYTES: u64 = 10 * 1024 * 1024; // 10MB limit for daemon response
    use std::io::Read;
    reader
        .by_ref()
        .take(MAX_RESPONSE_BYTES)
        .read_line(&mut line)?;
    let response = serde_json::from_str::<DaemonResponse>(&line)?;
    Ok(response)
}

/// Run a one-time protocol version check before the first RPC in a process lifetime.
/// Warns on mismatch but never exits — preserves backward compatibility.
fn check_version_once() {
    // Swap returns the previous value; if it was already true we've checked before.
    if VERSION_CHECKED.swap(true, Ordering::Relaxed) {
        return;
    }
    // Use send_raw to avoid recursive call through send_request.
    let resp = match send_raw(DaemonRequest::GetVersion) {
        Ok(r) => r,
        Err(_) => return, // Daemon unreachable — the real command will surface a better error.
    };
    if let DaemonResponse::VersionInfo { protocol, .. } = resp {
        if protocol != PROTOCOL_VERSION {
            eprintln!(
                "Warning: protocol version mismatch — daemon={protocol}, ctl={PROTOCOL_VERSION}"
            );
            if protocol > PROTOCOL_VERSION {
                eprintln!(
                    "  The daemon is newer. Some commands may fail. Consider updating apollo-optimizerctl."
                );
            }
        }
    }
}

fn send_request(req: DaemonRequest) -> anyhow::Result<DaemonResponse> {
    check_version_once();
    send_raw(req)
}

fn handle_dashboard() -> anyhow::Result<()> {
    // Live TUI watch loop: re-renders in-place at ~4 Hz (250 ms cadence).
    // Uses cursor-home overwrite (no clear) + differential line rendering to
    // eliminate all visible flicker. [btop++ 2022] in-place terminal redraw;
    // [Marcotte 2010] "Repainting Only What Changed" — TUI diff patterns.
    use std::io::Write;
    let stdout = std::io::stdout();

    // Hide cursor for the duration of the watch session.
    print!("\x1b[?25l");
    stdout.lock().flush().ok();

    // Restore cursor + clear on Ctrl-C.
    ctrlc::set_handler(|| {
        print!("\x1b[?25h");
        let _ = std::io::stdout().flush();
        std::process::exit(0);
    })
    .ok();

    // Probe first frame: check daemon is reachable before entering loop.
    let first = send_raw(DaemonRequest::GetStatus)
        .context("No se pudo conectar al daemon. ¿Está corriendo apollo-optimizerd?")?;
    let mut prev_frame: Vec<String> = Vec::new();
    // Status hash: skip render entirely when daemon reports identical state.
    // Uses a cheap XOR fold over the JSON bytes — not cryptographic, just change-detection.
    // [Knuth 1997] "The Art of Computer Programming" §3.3 — hash for equality probe.
    let mut prev_hash: u64 = 0;

    let hash_status = |status: &apollo_optimizer::engine::types::DaemonStatus| -> u64 {
        // Serialize to JSON and fold bytes with a Fowler-Noll-Vo-inspired XOR hash.
        // Fast (< 10µs for ~2KB JSON), deterministic, zero-alloc on repeat calls.
        let json = serde_json::to_string(status).unwrap_or_default();
        let mut h: u64 = 0xcbf2_9ce4_8422_2325; // FNV-1a offset basis
        for b in json.bytes() {
            h ^= b as u64;
            h = h.wrapping_mul(0x0000_0100_0000_01b3); // FNV prime
        }
        h
    };

    let render_one = |status: &apollo_optimizer::engine::types::DaemonStatus,
                      prev: &mut Vec<String>,
                      prev_h: &mut u64| {
        let h = hash_status(status);
        if h == *prev_h && !prev.is_empty() {
            return; // Identical state — skip rendering entirely.
        }
        *prev_h = h;
        let frame = dashboard::render_dashboard(status);
        let new_lines: Vec<String> = frame.lines().map(|l| l.to_string()).collect();

        // Build the entire output as a single string, then flush once.
        // Single write = no tearing from partial flushes mid-render.
        // [Unix write(2)] write() on a pipe/tty is atomic up to PIPE_BUF (4KB on Linux,
        // unlimited on macOS for local sockets). Our box is ~70×60 = ~4200 bytes with
        // ANSI codes — fits in one syscall on macOS. [POSIX.1-2017] §2.9.7.
        let mut buf = String::with_capacity(8192);

        // First frame or line count changed: full redraw from home.
        if prev.is_empty() || prev.len() != new_lines.len() {
            buf.push_str("\x1b[H");
            for line in &new_lines {
                buf.push_str(line);
                buf.push_str("\x1b[K\r\n");
            }
        } else {
            // Differential: only rewrite lines that changed.
            // \x1b[K after content ensures stale chars from longer old lines are cleared.
            let mut changed = 0usize;
            for (row, (new, old)) in new_lines.iter().zip(prev.iter()).enumerate() {
                if new != old {
                    buf.push_str(&format!("\x1b[{};1H{}\x1b[K", row + 1, new));
                    changed += 1;
                }
            }
            if changed == 0 {
                // Nothing changed — skip the write entirely (zero CPU, zero flicker).
                *prev = new_lines;
                return;
            }
        }
        use std::io::Write;
        stdout.lock().write_all(buf.as_bytes()).ok();
        stdout.lock().flush().ok();
        *prev = new_lines;
    };

    // Render first frame.
    if let DaemonResponse::Status(s) = first {
        // Clear screen once at startup to establish a clean canvas.
        print!("\x1b[2J");
        render_one(&s, &mut prev_frame, &mut prev_hash);
    }

    loop {
        std::thread::sleep(std::time::Duration::from_millis(250));
        match send_raw(DaemonRequest::GetStatus) {
            Ok(DaemonResponse::Status(s)) => render_one(&s, &mut prev_frame, &mut prev_hash),
            Ok(DaemonResponse::Error { message }) => {
                // Daemon error: show inline, keep looping.
                print!("\x1b[H\x1b[31mDaemon error: {}\x1b[0m", message);
                stdout.lock().flush().ok();
            }
            Err(_) => {
                // Transient disconnect: show status, keep looping.
                print!("\x1b[H\x1b[33mWaiting for daemon...\x1b[0m");
                stdout.lock().flush().ok();
            }
            _ => {}
        }
    }
}

fn handle_teach_export() -> anyhow::Result<()> {
    // 1. Get current policy
    let policy = match send_request(DaemonRequest::GetLearnedPolicy)? {
        DaemonResponse::LearnedPolicy(p) => p,
        DaemonResponse::Error { message } => anyhow::bail!(message),
        _ => anyhow::bail!("unexpected response"),
    };

    // 2. Get usage model top entries
    let usage = match send_request(DaemonRequest::UsageTop { limit: Some(30) })? {
        DaemonResponse::Usage(UsageResponse::Top(report)) => report,
        DaemonResponse::Error { message } => anyhow::bail!(message),
        _ => anyhow::bail!("unexpected response"),
    };

    // 3. Get system status
    let status = match send_request(DaemonRequest::GetStatus)? {
        DaemonResponse::Status(s) => s,
        DaemonResponse::Error { message } => anyhow::bail!(message),
        _ => anyhow::bail!("unexpected response"),
    };

    // 4. Format output for Claude
    println!("# Apollo Optimizer — LLM Teacher Export");
    println!("# Timestamp: {}", chrono::Utc::now().to_rfc3339());
    println!(
        "# Paste this output to Claude Code and ask it to analyze and generate a clean policy."
    );
    println!();

    println!(
        "## Current Learned Policy ({} interactive, {} noise, {} protected)",
        policy.interactive_patterns.len(),
        policy.noise_patterns.len(),
        policy.protected_patterns.len(),
    );
    println!("Interactive: {}", policy.interactive_patterns.join(", "));
    if !policy.noise_patterns.is_empty() {
        println!("Noise: {}", policy.noise_patterns.join(", "));
    }
    if !policy.protected_patterns.is_empty() {
        println!("Protected: {}", policy.protected_patterns.join(", "));
    }
    println!();

    println!("## Usage Model — Top 30 by Usage Score (candidates for interactive)");
    println!(
        "{:<3} {:<40} {:>6} {:>6} {:>6} {:>6} {:>8} {:>8} {:>6}",
        "#", "Name", "usage", "pres", "inter", "jank", "cpu", "mem", "seen"
    );
    for (i, e) in usage.interactive_candidates.iter().enumerate() {
        println!(
            "{:<3} {:<40} {:>6.3} {:>6.3} {:>6.3} {:>6.3} {:>8.5} {:>8.5} {:>6}",
            i + 1,
            e.name,
            e.usage_score,
            e.presence_ema,
            e.interactive_ema,
            e.jank_ema,
            e.cpu_ema,
            e.mem_ema,
            e.first_seen_at.map(|_| "y").unwrap_or("-"),
        );
    }
    println!();

    println!("## Usage Model — Top Noise Candidates");
    println!(
        "{:<3} {:<40} {:>6} {:>6} {:>8} {:>8}",
        "#", "Name", "noise", "jank", "cpu", "mem"
    );
    for (i, e) in usage.noise_candidates.iter().enumerate() {
        println!(
            "{:<3} {:<40} {:>6.3} {:>6.3} {:>8.5} {:>8.5}",
            i + 1,
            e.name,
            e.noise_score,
            e.jank_ema,
            e.cpu_ema,
            e.mem_ema,
        );
    }
    println!();

    println!("## System Metrics");
    println!(
        "Memory pressure: {:.2} | Swap: {:.1} GB | Thermal: {}",
        status.metrics.memory_pressure,
        status.metrics.swap_used_bytes as f64 / (1024.0 * 1024.0 * 1024.0),
        status.metrics.thermal_state,
    );
    println!(
        "Workload: {} (confidence: {:.2})",
        status.metrics.current_workload, status.metrics.ml_confidence
    );
    println!(
        "Profile: {:?} | Cycles: {} | QoS errors: {}",
        status.effective_profile, status.metrics.cycles, status.metrics.qos_errors
    );
    println!();

    println!("## Instructions for Claude");
    println!("Analyze the usage model entries and current policy.");
    println!("Determine which processes are genuinely interactive (user-facing apps,");
    println!("latency-sensitive audio/video/input) vs background daemons that just");
    println!("happen to run frequently alongside user activity (correlation != causation).");
    println!();
    println!("Output a JSON file with this exact structure:");
    println!("```json");
    println!("{{");
    println!("  \"interactive_patterns\": [\"...\"],");
    println!("  \"noise_patterns\": [\"...\"],");
    println!("  \"protected_patterns\": [],");
    println!("  \"learned_at\": \"{}\"", chrono::Utc::now().to_rfc3339());
    println!("}}");
    println!("```");
    println!();
    println!("Then apply with: apollo-optimizerctl teach apply <file.json>");

    Ok(())
}

fn handle_teach_apply(file: &str) -> anyhow::Result<()> {
    use apollo_optimizer::engine::types::HardPath;
    let content = HardPath::read_to_string_limited(Path::new(file), 1024 * 1024)
        .with_context(|| format!("cannot read file or limit exceeded: {}", file))?;
    let policy: LearnedPolicy = serde_json::from_str(&content)
        .with_context(|| format!("invalid learned_policy JSON in: {}", file))?;

    let interactive_count = policy.interactive_patterns.len();
    let noise_count = policy.noise_patterns.len();

    let response = send_request(DaemonRequest::SetLearnedPolicy { policy })?;
    match response {
        DaemonResponse::Ok => {
            println!(
                "Policy applied: {} interactive, {} noise patterns",
                interactive_count, noise_count
            );
            println!("The daemon has updated its in-memory state and persisted to disk.");
        }
        DaemonResponse::Error { message } => anyhow::bail!(message),
        _ => anyhow::bail!("unexpected response"),
    }
    Ok(())
}

fn main() -> anyhow::Result<()> {
    let cli = Cli::parse();

    if matches!(&cli.command, Commands::Dashboard) {
        return handle_dashboard();
    }

    let response = match cli.command {
        Commands::Dashboard => unreachable!(),
        Commands::Status => send_request(DaemonRequest::GetStatus),
        Commands::Metrics => send_request(DaemonRequest::GetMetrics),
        Commands::TopBlockers => send_request(DaemonRequest::GetTopBlockers),
        Commands::ProfileTimeline => send_request(DaemonRequest::GetProfileTimeline),
        Commands::Doctor => send_request(DaemonRequest::Doctor),
        Commands::Capabilities => send_request(DaemonRequest::GetCapabilities),
        Commands::Restore => send_request(DaemonRequest::Restore),
        Commands::PanicRestore => send_request(DaemonRequest::PanicRestore),
        Commands::Pause => {
            let path = if unsafe { libc::geteuid() } == 0 {
                "/var/run/apollo.disable"
            } else {
                "/tmp/apollo.disable"
            };
            let _ = std::fs::File::create(path);
            println!("Apollo paused. Optimization suspended.");
            println!("To resume: apollo-optimizerctl resume");
            return Ok(());
        }
        Commands::Resume => {
            // Try both paths — the kill switch may have been created by root
            // even if we're running without sudo now
            let kill_paths = ["/var/run/apollo.disable", "/tmp/apollo.disable"];
            let mut removed = false;
            for path in &kill_paths {
                if std::path::Path::new(path).exists() {
                    match std::fs::remove_file(path) {
                        Ok(_) => {
                            println!("Removed kill switch: {}", path);
                            removed = true;
                        }
                        Err(e) if e.kind() == std::io::ErrorKind::PermissionDenied => {
                            eprintln!(
                                "Error: cannot remove {} — permission denied. Try: sudo apollo-optimizerctl resume",
                                path
                            );
                            std::process::exit(1);
                        }
                        Err(_) => {}
                    }
                }
            }
            if removed {
                println!("Apollo resumed. Optimization active.");
            } else {
                println!("Apollo was not paused (no kill switch found).");
            }
            return Ok(());
        }
        Commands::IsPaused => {
            let kill_paths = ["/var/run/apollo.disable", "/tmp/apollo.disable"];
            let mut paused = false;
            for path in &kill_paths {
                if std::path::Path::new(path).exists() {
                    println!("PAUSED — kill switch active: {}", path);
                    paused = true;
                }
            }
            if paused {
                println!("To resume: sudo apollo-optimizerctl resume");
            } else {
                println!("ACTIVE — optimization running normally");
            }
            return Ok(());
        }
        Commands::SetAutoProfile { enabled } => send_request(DaemonRequest::SetAutoProfile {
            enabled: enabled == "on",
        }),
        Commands::ClearProfileOverride => send_request(DaemonRequest::ClearProfileOverride),
        Commands::SetProfile {
            profile,
            ttl_minutes,
        } => send_request(DaemonRequest::SetProfile {
            profile: to_profile(&profile),
            ttl_minutes: Some(ttl_minutes),
        }),
        Commands::SetLatencyTarget { target } => send_request(DaemonRequest::SetLatencyTarget {
            target: to_latency_target(&target),
        }),
        Commands::Llm { command } => match command {
            LlmCommands::Status => send_request(DaemonRequest::GetLlmStatus),
            LlmCommands::Disable => send_request(DaemonRequest::LlmDisable),
            LlmCommands::Test => send_request(DaemonRequest::LlmTest),
            LlmCommands::SetKey { key, ttl_days } => {
                let key = key
                    .or_else(|| std::env::var("APOLLO_LLM_API_KEY").ok())
                    .context("missing API key: pass --key or set APOLLO_LLM_API_KEY")?;
                send_request(DaemonRequest::LlmSetKey {
                    api_key: key,
                    ttl_days,
                })
            }
        },
        Commands::DumpPolicy => send_request(DaemonRequest::GetLearnedPolicy),
        Commands::Feedback { rating, note } => {
            send_request(DaemonRequest::Feedback { rating, note })
        }
        Commands::Usage { command } => match command {
            UsageCommands::Top { limit } => {
                send_request(DaemonRequest::UsageTop { limit: Some(limit) })
            }
            UsageCommands::Explain { name } => send_request(DaemonRequest::UsageExplain { name }),
        },
        Commands::Teach { command } => match command {
            TeachCommands::Export => return handle_teach_export(),
            TeachCommands::Apply { file } => return handle_teach_apply(&file),
        },
        Commands::SysctlGovernor => send_request(DaemonRequest::GetSysctlGovernor),
        Commands::RevertSysctls => send_request(DaemonRequest::RevertSysctls),
        Commands::Version => send_request(DaemonRequest::GetVersion),
        Commands::Health => send_request(DaemonRequest::GetHealth),
    }?;

    match response {
        DaemonResponse::Ok => println!("ok"),
        DaemonResponse::Status(s) => println!("{}", serde_json::to_string_pretty(&s)?),
        DaemonResponse::StatusPush(s) => println!("{}", serde_json::to_string_pretty(&s)?),
        DaemonResponse::Metrics(m) => println!("{}", serde_json::to_string_pretty(&m)?),
        DaemonResponse::TopBlockers(b) => println!("{}", serde_json::to_string_pretty(&b)?),
        DaemonResponse::ProfileTimeline(t) => println!("{}", serde_json::to_string_pretty(&t)?),
        DaemonResponse::Capabilities(c) => println!("{}", serde_json::to_string_pretty(&c)?),
        DaemonResponse::LlmStatus(s) => println!("{}", serde_json::to_string_pretty(&s)?),
        DaemonResponse::LlmTestResult {
            ok,
            http_status,
            error,
            suggestion,
        } => {
            println!(
                "{}",
                serde_json::to_string_pretty(&serde_json::json!({
                    "ok": ok,
                    "http_status": http_status,
                    "error": error,
                    "suggestion": suggestion,
                }))?
            )
        }
        DaemonResponse::LearnedPolicy(p) => println!("{}", serde_json::to_string_pretty(&p)?),
        DaemonResponse::Usage(u) => println!("{}", serde_json::to_string_pretty(&u)?),
        DaemonResponse::SysctlGovernor(s) => println!("{}", serde_json::to_string_pretty(&s)?),
        DaemonResponse::VersionInfo { protocol, build } => {
            println!("apollo-optimizer v{build}  (protocol v{protocol})");
            if protocol != PROTOCOL_VERSION {
                eprintln!(
                    "warning: protocol mismatch — daemon uses v{protocol}, client uses v{PROTOCOL_VERSION}"
                );
            }
        }
        DaemonResponse::Doctor { checks } => {
            for c in checks {
                println!("{}", c);
            }
            let _ = fs::metadata("/var/run/apollo-optimizer.sock");
            let _ = fs::metadata("/tmp/apollo-optimizer.sock");
        }
        DaemonResponse::Health(h) => println!("{}", serde_json::to_string_pretty(&h)?),
        DaemonResponse::Error { message } => {
            anyhow::bail!(message);
        }
    }

    Ok(())
}
