//! Optimization Skills — persistent recipes learned from experience.
//!
//! Inspired by Hermes Agent's self-improving skills system.
//! When Apollo discovers a sequence of actions that reliably reduces pressure,
//! it saves it as a "skill" — a reusable recipe with measured success rate.
//!
//! Skills are persisted to disk and loaded on startup, surviving restarts.
//! Each skill has a trigger condition and a measured effectiveness.

use serde::{Deserialize, Serialize};
use std::collections::HashMap;
use std::path::Path;

/// Origin of an optimization skill: individually learned or batch-induced.
///
/// Seed type for future refactors — does not affect the current public interface.
/// Individual skills are learned from direct throttle-outcome observations.
/// Induced skills are generated by the rule inducer from co-occurrence patterns.
#[derive(Clone, Debug, PartialEq, Eq, Serialize, Deserialize)]
pub enum SkillOrigin {
    Individual,
    Induced,
}

impl SkillOrigin {
    /// Infer origin from skill name convention: "group:" and "batch:" prefixes
    /// indicate induced skills; anything else is individually learned.
    pub fn from_name(name: &str) -> Self {
        if name.starts_with("group:") || name.starts_with("batch:") {
            SkillOrigin::Induced
        } else {
            SkillOrigin::Individual
        }
    }
}

/// A learned optimization recipe.
#[derive(Clone, Debug, Serialize, Deserialize)]
pub struct OptimizationSkill {
    /// Human-readable name (e.g., "cloud_sync_throttle").
    pub name: String,
    /// Trigger: minimum pressure to activate.
    ///
    /// BUG-03 audit: callers cast f64 memory_pressure to f32 before comparison.
    /// The f64→f32 cast introduces ±2.4e-8 jitter (half an f32 ULP at 0.5–0.8).
    /// At threshold values in the 0.55–0.70 range, this is ~3×10⁻⁸ relative error —
    /// negligible compared to the ±0.01 pressure noise floor. Keeping f32 avoids a
    /// breaking change to the serialized `optimization_skills.json` schema.
    pub min_pressure: f32,
    /// Trigger: workload pattern (e.g., "Browser", "Build").
    pub workload_hint: String,
    /// Action: process names to throttle.
    pub throttle_targets: Vec<String>,
    /// Measured success rate [0, 1].
    pub success_rate: f32,
    /// Total times applied.
    pub apply_count: u32,
    /// Total times pressure dropped after applying.
    pub success_count: u32,
}

impl OptimizationSkill {
    /// Record an application result.
    pub fn record(&mut self, was_effective: bool) {
        self.apply_count += 1;
        if was_effective {
            self.success_count += 1;
        }
        self.success_rate = self.success_count as f32 / self.apply_count.max(1) as f32;
    }

    /// Is this skill reliable? (≥5 applications, ≥60% success rate)
    pub fn is_reliable(&self) -> bool {
        self.apply_count >= 5 && self.success_rate >= 0.60
    }

    /// Should this skill be retired?
    ///
    /// Two conditions:
    /// - **Primary** (≥10 obs, <35%): clearly ineffective — below noise floor.
    /// - **Zombie** (≥20 obs, <50%): consistently mediocre — never crosses the
    ///   reliable threshold (60%) and occupies a trial slot indefinitely.
    pub fn should_retire(&self) -> bool {
        (self.apply_count >= 10 && self.success_rate < 0.35)
            || (self.apply_count >= 20 && self.success_rate < 0.50)
    }

    /// EMA-update min_pressure toward the pressure where this skill
    /// was actually applied (α = 0.20).  Called after effective trials
    /// so the skill self-calibrates to its real operating range.
    pub fn adapt_pressure(&mut self, pressure: f32) {
        self.min_pressure = self.min_pressure * 0.80 + pressure * 0.20;
    }
}

/// Returns true if the process name looks like an Apple OS daemon or system
/// infrastructure process that should never become a skill target.
///
/// This is a structural heuristic on top of the explicit `protected_processes()`
/// list — it catches system processes we haven't explicitly listed yet, based on
/// macOS naming conventions:
/// - `com.apple.*`  — Apple reverse-domain services (XPC, agents, extensions)
/// - `*DriverKit*`  — DriverKit user-space drivers
/// - `org.pqrs.*`   — Karabiner / third-party kernel extensions
/// - `kernel_task`  — macOS kernel placeholder process
/// - `launchd`      — PID 1, never throttleable
fn is_system_process_name(name: &str) -> bool {
    name.starts_with("com.apple.")
        || name.starts_with("org.pqrs.")
        || name.contains("DriverKit")
        || name == "kernel_task"
        || name == "launchd"
}

/// Registry of learned optimization skills.
pub struct SkillRegistry {
    skills: HashMap<String, OptimizationSkill>,
}

impl Default for SkillRegistry {
    fn default() -> Self {
        Self::new()
    }
}

impl SkillRegistry {
    pub fn new() -> Self {
        Self {
            skills: HashMap::new(),
        }
    }

    /// Learn a new skill from observed effective throttle patterns.
    pub fn learn(&mut self, name: &str, pressure: f32, workload: &str, targets: Vec<String>) {
        // Filter protected OS daemons and system processes at ingestion time.
        // Two-layer check:
        // 1. Unified exact-match oracle (safety::is_protected_name) — covers
        //    Tier 1 OS essentials, Tier 2 infra services, Tier 3 dev runtimes.
        //    Replaces previous substring scan which over-protected (e.g.,
        //    "WindowServer-helper" matched "WindowServer" as false-positive).
        //    Saltzer & Kaashoek 2009 §3.3 Complete Mediation.
        // 2. Structural heuristic: Apple/system naming patterns that signal
        //    OS infrastructure — these should never be skill targets regardless
        //    of observed correlations.
        let clean_targets: Vec<String> = targets
            .into_iter()
            .filter(|t| !crate::engine::safety::is_protected_name(t) && !is_system_process_name(t))
            .collect();
        if clean_targets.is_empty() {
            return;
        }
        let skill = self
            .skills
            .entry(name.to_string())
            .or_insert_with(|| OptimizationSkill {
                name: name.to_string(),
                min_pressure: pressure,
                workload_hint: workload.to_string(),
                throttle_targets: clean_targets.clone(),
                success_rate: 0.5,
                apply_count: 0,
                success_count: 0,
            });
        // Update pressure threshold with EMA.
        skill.min_pressure = skill.min_pressure * 0.9 + pressure * 0.1;
    }

    /// Record result for a skill.
    pub fn record_result(&mut self, name: &str, was_effective: bool) {
        if let Some(skill) = self.skills.get_mut(name) {
            skill.record(was_effective);
        }
    }

    /// Record result and, on success, EMA-adapt the skill's pressure threshold
    /// toward the pressure at which it was applied.  Use this in the trial loop
    /// so skills converge to their real operating range over time.
    pub fn record_result_with_pressure(&mut self, name: &str, was_effective: bool, pressure: f32) {
        if let Some(skill) = self.skills.get_mut(name) {
            skill.record(was_effective);
            if was_effective {
                skill.adapt_pressure(pressure);
            }
        }
    }

    /// Get reliable skills matching current conditions.
    pub fn matching_skills(&self, pressure: f32, workload: &str) -> Vec<&OptimizationSkill> {
        self.skills
            .values()
            .filter(|s| {
                s.is_reliable()
                    && pressure >= s.min_pressure
                    && (s.workload_hint == workload || s.workload_hint == "any")
            })
            .collect()
    }

    /// Pick one unproven induced skill to trial this cycle.
    ///
    /// Induced skills (group:/batch:) start with apply_count=0 and can never
    /// reach is_reliable() without being applied first. This method returns
    /// the single highest-priority unproven skill at current pressure so it
    /// gets one real observation per call. Caller records the result via
    /// record_result() after measuring pressure delta.
    ///
    /// Criteria: apply_count < 10, pressure >= min_pressure, not retired,
    /// workload matches (skill.workload_hint == "any" OR == current workload).
    /// Priority: fewest applications first (round-robin exploration).
    pub fn next_trial_skill(&self, pressure: f32, workload: &str) -> Option<&OptimizationSkill> {
        self.skills
            .values()
            .filter(|s| {
                s.apply_count < 10
                    && !s.should_retire()
                    && pressure >= s.min_pressure
                    && (s.name.starts_with("group:") || s.name.starts_with("batch:"))
                    && (s.workload_hint == "any" || s.workload_hint == workload)
            })
            .min_by_key(|s| s.apply_count)
    }

    /// Remove induced skills whose ALL throttle targets are protected.
    /// These skills can never execute and would spin forever in the trial loop.
    /// `protected` is the combined hard + policy protected set.
    pub fn purge_unexecutable(&mut self, protected: &[&str]) {
        self.skills.retain(|name, skill| {
            if !name.starts_with("group:") && !name.starts_with("batch:") {
                return true; // keep individual skills unconditionally
            }
            // Keep if at least one target is NOT protected.
            skill.throttle_targets.iter().any(|target| {
                let tl = target.to_ascii_lowercase();
                !protected
                    .iter()
                    .any(|p| tl.contains(&p.to_ascii_lowercase()))
            })
        });
    }

    /// Register an autonomously induced skill (from rule_inducer).
    /// Skips if a skill with the same name already exists.
    /// Returns true if the skill was added.
    pub fn register_induced(&mut self, skill: OptimizationSkill) -> bool {
        if self.skills.contains_key(&skill.name) {
            return false;
        }
        self.skills.insert(skill.name.clone(), skill);
        true
    }

    /// Snapshot of all skill names (for duplicate checking in rule_inducer).
    pub fn name_set(&self) -> std::collections::HashSet<String> {
        self.skills.keys().cloned().collect()
    }

    /// Cross-feed: boost a skill's success_count when external Bayesian evidence
    /// (e.g., OutcomeTracker or CausalGraph) is stronger than trial data.
    ///
    /// Only applies if:
    ///   - The skill exists.
    ///   - `evidence_rate` exceeds the current `success_rate` by at least `min_gap`.
    ///   - The skill has ≥ `min_apply_count` applications (enough to be meaningful).
    ///
    /// Returns `true` if a boost was applied.
    pub fn cross_feed_boost(
        &mut self,
        name: &str,
        evidence_rate: f64,
        min_gap: f64,
        min_apply_count: u32,
    ) -> bool {
        if let Some(skill) = self.skills.get_mut(name) {
            if skill.apply_count >= min_apply_count
                && (evidence_rate - skill.success_rate as f64) >= min_gap
            {
                skill.success_count = skill.success_count.saturating_add(1);
                skill.apply_count = skill.apply_count.saturating_add(1);
                skill.success_rate = skill.success_count as f32 / skill.apply_count.max(1) as f32;
                return true;
            }
        }
        false
    }

    /// Query the success_rate for a skill by name. Returns `None` if not found.
    pub fn success_rate(&self, name: &str) -> Option<f32> {
        self.skills.get(name).map(|s| s.success_rate)
    }

    /// Query apply_count for a skill by name. Returns `None` if not found.
    pub fn apply_count(&self, name: &str) -> Option<u32> {
        self.skills.get(name).map(|s| s.apply_count)
    }

    /// Skill-Aware Prediction signal (Phase 3.1 — Sprint 6).
    ///
    /// Aggregates the historical success of reliable skills matching the given
    /// workload into a single [0, 1] signal. Used by `apply_specialist_voting`
    /// to modulate the confidence of non-Observe interventions: in workloads
    /// where past throttle-class actions have empirically worked, specialist
    /// votes are amplified; where they have failed, votes are damped.
    ///
    /// Weighting: each skill contributes `success_rate * apply_count`, so a
    /// skill with 100 applications dominates one with 5 — more evidence wins.
    /// Skills with `workload_hint == "any"` are included for every workload.
    ///
    /// Returns `None` when no reliable skill matches — caller should treat
    /// this as "no signal" and leave the vote weight at 1.0 (neutral).
    pub fn workload_success_signal(&self, workload: &str) -> Option<f32> {
        let mut weighted_sum = 0.0_f32;
        let mut weight_total = 0.0_f32;
        for s in self.skills.values() {
            if !s.is_reliable() {
                continue;
            }
            if s.workload_hint != "any" && s.workload_hint != workload {
                continue;
            }
            let w = s.apply_count as f32;
            weighted_sum += s.success_rate * w;
            weight_total += w;
        }
        if weight_total == 0.0 {
            None
        } else {
            Some((weighted_sum / weight_total).clamp(0.0, 1.0))
        }
    }

    /// Retire ineffective skills.
    pub fn gc(&mut self) {
        self.skills.retain(|_, s| !s.should_retire());
    }

    /// Number of skills.
    pub fn len(&self) -> usize {
        self.skills.len()
    }

    /// Number of reliable skills.
    pub fn reliable_count(&self) -> usize {
        self.skills.values().filter(|s| s.is_reliable()).count()
    }

    /// Snapshot all skills as a cloned map — used by LearnedState::collect().
    pub fn snapshot(&self) -> HashMap<String, OptimizationSkill> {
        self.skills.clone()
    }

    /// Restore skills from a persisted map — used by LearnedState::apply().
    /// Replaces the entire registry; existing skills are discarded.
    pub fn restore_from_map(&mut self, map: HashMap<String, OptimizationSkill>) {
        // Purge skills whose keys reference protected OS daemons or system
        // process naming patterns. Guards against stale disk state from before
        // these protections were added.
        // Migrated from substring scan over protected_processes() to the
        // unified is_protected_name() oracle (Tier 1/2/3) — closes
        // "WindowServer-helper" false-positive class. Saltzer & Kaashoek 2009
        // §3.3 Complete Mediation.
        self.skills = map
            .into_iter()
            .filter(|(k, _)| {
                // Strip skill prefix (throttle:/group:/induced:) to get process name
                let proc_name = k
                    .trim_start_matches("throttle:")
                    .trim_start_matches("group:")
                    .trim_start_matches("induced:");
                !crate::engine::safety::is_protected_name(proc_name)
                    && !is_system_process_name(proc_name)
            })
            .collect();
    }

    /// Persist to disk.
    pub fn persist(&self, path: &Path) {
        if let Ok(json) = serde_json::to_string(&self.skills) {
            let _ = std::fs::write(path, json);
        }
    }

    /// Load from disk.
    pub fn load(&mut self, path: &Path) {
        if let Ok(data) = std::fs::read_to_string(path) {
            if let Ok(skills) = serde_json::from_str::<HashMap<String, OptimizationSkill>>(&data) {
                self.skills = skills;
            }
        }
    }
}

