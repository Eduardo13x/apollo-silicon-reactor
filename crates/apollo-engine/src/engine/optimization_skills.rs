    #[test]
    fn test_skill_reliability() {
        let mut s = OptimizationSkill {
            name: "test".into(),
            min_pressure: 0.70,
            workload_hint: "any".into(),
            throttle_targets: vec!["Dropbox".into()],
            success_rate: 0.0,
            apply_count: 0,
            success_count: 0,
        };
        for _ in 0..8 {
            s.record(true);
        }
        for _ in 0..2 {
            s.record(false);
        }
        assert!(s.is_reliable()); // 80% success
        assert!(!s.should_retire());
    }

